// --- Supabase helper functions for daily tasks and emotional check ---
import { supabase } from "@/lib/supabase";

export interface DailyTask {
  id: string;
  user_id: string;
  dia_numero: number;
  task_number: number;
  description: string;
  completed: boolean;
}

/** Fetch tasks for a given user and day */
export async function fetchTasks(userId: string, diaNumero: number): Promise<DailyTask[]> {
  const { data, error } = await supabase
    .from("tasks")
    .select("*")
    .eq("user_id", userId)
    .eq("dia_numero", diaNumero)
    .order("task_number", { ascending: true });
  if (error) throw error;
  return data as DailyTask[];
}

/** Upsert tasks (replace existing tasks for the day) */
export async function upsertTasks(
  userId: string,
  diaNumero: number,
  tasks: { task_number: number; description: string }[]
) {
  await supabase.from("tasks").delete().eq("user_id", userId).eq("dia_numero", diaNumero);
  const inserts = tasks
    .filter((t) => t.description.trim().length > 0)
    .map((t) => ({
      user_id: userId,
      dia_numero: diaNumero,
      task_number: t.task_number,
      description: t.description,
      completed: false,
    }));
  if (inserts.length === 0) return;
  const { error } = await supabase.from("tasks").insert(inserts);
  if (error) throw error;
}

/** Save emotional check (0=triste, 1=neutro, 2=feliz) */
export async function saveEmotionalCheck(userId: string, diaNumero: number, level: number) {
  const { error } = await supabase.from("emotional_checks").upsert(
    { user_id: userId, dia_numero: diaNumero, level },
    { onConflict: "user_id,dia_numero" }
  );
  if (error) throw error;
}
